                <h1>Integrating PHP and MySQL</h1>
                <p>Connecting your website to a MySQL database is fairly simple in PHP.</p>
                <p>You'll need to create a Connection object and provide it the correct information.</p>
                <p>Here is an example:</p>
                <pre><code>
        try {
            $connection = new PDO("mysql:host=$servername;dbname=$database", $username, $password);
            $connection->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
            print 'Connected Successfully';
        } catch (PDOException $E) {
            print 'Connection failed';
            print $e->getMessage();
        }
                </code></pre>
                <p>The above will connect to the server and database provided with the given credentials.</p>
                <p>You can now use this connection to run MySQL statements.</p>
